package com.pulse.payload;

public class URLPayload {
    private String url;
    private String status;



    public URLPayload() {
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}

